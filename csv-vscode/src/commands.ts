/**
 * Implements the "Copy Right ID" command.
 */
async function copyRightIdCommand(rightId?: string): Promise<void> {
  if (!rightId) {
    vscode.window.showWarningMessage("No Right ID provided to copy");
    return;
  }

  await vscode.env.clipboard.writeText(rightId);
  vscode.window.showInformationMessage(
    `Copied Right ID: ${rightId.substring(0, 10)}...`
  );
}
